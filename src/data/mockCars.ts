export const getCarById = (id: string): CarType | undefined => {
    return mockCars.find((car) => car.id === id);
};

export const getCarsByCategory = (categoryId: string): CarType[] => {
    return mockCars.filter((car) => car.category === categoryId);
};

export const searchCars = (query: string): CarType[] => {
    const lowerQuery = query.toLowerCase();
    return mockCars.filter(
        (car) =>
            car.brand.toLowerCase().includes(lowerQuery) ||
            car.model.toLowerCase().includes(lowerQuery) ||
            car.location.toLowerCase().includes(lowerQuery)
    );
};
